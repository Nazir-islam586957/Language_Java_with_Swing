    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// submit

        if (id_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Id IS empty......");
            id_txt.requestFocus();
        } else if (name_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Name IS EMpty");
            name_txt.requestFocus();
        } else if (mail_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Mail IS EMpty");
            mail_txt.requestFocus();
        } else if (pass_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Pass IS EMpty");
            pass_txt.requestFocus();
        } else {
            try {
                contest();
                String inQueary = "INSERT INTO `registration` (`id`,`name`,`mail`,`password`) VALUES (?,?,?,?)";
                ps = ct.prepareCall(inQueary);
                ps.setString(1, id_txt.getText());
                ps.setString(2, name_txt.getText());
                ps.setString(3, mail_txt.getText());
                ps.setString(4, pass_txt.getText());
                int m = ps.executeUpdate();
                if (m > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Success");
                    SelectAllData();
                    autoCount();
                    clearDAta();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Failed");

                }
            } catch (SQLException ex) {
                Logger.getLogger(AutoIncrement.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

