    void SelectAllData() {
        try {
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            dt.setRowCount(0);
            String selectQueary = " SELECT `id`,`name`,`mail`,`password` FROM `registration`";
            contest();
            st = ct.prepareCall(selectQueary);
            rs = st.executeQuery(selectQueary);
            while (rs.next()) {
                dt.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4)
                });

            }
        } catch (SQLException ex) {
            Logger.getLogger(AutoIncrement.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    void autoCount() {
        int c = 0;
        try {
            contest();
            String idSelect = " SELECT `id`  FROM `registration`";
            st = ct.prepareCall(idSelect);
            rs = st.executeQuery(idSelect);
            while (rs.next()) {
               c =rs.getInt(1);
            }
            c++;
            id_txt.setText(""+c);
        } catch (SQLException ex) {
            Logger.getLogger(AutoIncrement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

