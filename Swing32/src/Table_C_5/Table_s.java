    void dis(){
    
    if(jTextField1.getText().toString().isBlank()){
     JOptionPane.showMessageDialog(this, "Enter Name...");
     jTextField1.requestFocus();
     }else if(jComboBox2.getSelectedIndex()==0){
      JOptionPane.showMessageDialog(this, "Select Department...");
    jComboBox2.requestFocus();
      }else if(rm.isSelected()!=true && rf.isSelected()!=true){
      JOptionPane.showMessageDialog(this, "Select Shift...");
    rm.requestFocus();
    }                                        
        else if(jComboBox1.getSelectedIndex()==0){
     JOptionPane.showMessageDialog(this, "Enter Semister...");
     jComboBox1.requestFocus();
      }else{
      name=jTextField1.getText().toString();
      dep=jComboBox2.getSelectedItem().toString();
      
     if(rm.isSelected()){
       sh=rm.getText();
     }else if(rf.isSelected()){
     sh=rf.getText();
    }   
     sem=jComboBox1.getSelectedItem().toString();
     
     data_clr();
     }
    
    }
    
    
