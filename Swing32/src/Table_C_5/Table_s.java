    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // update
        dis();
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
      if(dtm.getRowCount()>0){
        dtm.setValueAt(name,jTable1.getSelectedRow(),0);
        dtm.setValueAt(dep,jTable1.getSelectedRow(),1);
        dtm.setValueAt(sh,jTable1.getSelectedRow(),2);
        dtm.setValueAt(sem,jTable1.getSelectedRow(),3);
   }
    
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // mouse Clicked
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        if(dtm.getRowCount()>0){
        name=dtm.getValueAt(jTable1.getSelectedRow(),0).toString();
        
        dep = dtm.getValueAt(jTable1.getSelectedRow(),1).toString();
        sh = dtm.getValueAt(jTable1.getSelectedRow(),2).toString();
        sem=dtm.getValueAt(jTable1.getSelectedRow(),3).toString();
       
       jTextField1.setText(name);
       jComboBox2.setSelectedItem(dep);
       jComboBox1.setSelectedItem(sem);
       
       if(rm.getText().equals(sh)){
          rm.setSelected(true);
        }
       else{rf.setSelected(true);}
        }
    }//GEN-LAST:event_jTable1MouseClicked
 
    void data_clr(){
   jComboBox1.setSelectedIndex(0);
 jComboBox2.setSelectedIndex(0);
  jTextField1.setText("");
 buttonGroup1.clearSelection();
 jTextField1.requestFocus();
    }
